using MethodsDrill;


namespace MethodsDrill
{
    public class Addition
    {
        public int AddTwoValues(int x, int y)
        {
            return x + y;
        }
        public int AddThreeValues(int x, int y, int z)
        {
            return x + y + z;
        }
        public int AddFourValues(int x, int y, int z, int i)
        {
            return x + y + z + i;
        }
    }
}